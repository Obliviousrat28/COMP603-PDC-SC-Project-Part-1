    private void btnRoleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRoleActionPerformed
        // 1. Get the row selection index from the actual JTable variable name
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Please select a user from the table first.", 
                "No Selection", 
                javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 2. Read the username string from Column 0 of the highlighted row
        String targetUsername = jTable1.getValueAt(selectedRow, 0).toString();
    
        // 2. Grab the exact username string of the logged-in admin user
        String loggedInAdmin = (this.currentUser != null) ? this.currentUser.getUsername() : "";

        // 3. Self-Demotion Safety Check against the active session user context
        if (targetUsername.equals(loggedInAdmin)) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Self-Demotion Lock: You cannot alter your own permissions directly via this screen panel.", 
                "Action Rejected", 
                javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 4. Extract the selected target enum value from the correct JComboBox name
        Object selectedItem = roleCombo.getSelectedItem();
        if (selectedItem == null) {
            return;
        }

        com.group51.servicedeskproject.model.Role newRole;

        // Safely parse the value whether it's an explicit Role Enum or a standard String selection
        if (selectedItem instanceof com.group51.servicedeskproject.model.Role) {
            newRole = (com.group51.servicedeskproject.model.Role) selectedItem;
        } else {
            try {
                newRole = com.group51.servicedeskproject.model.Role.valueOf(selectedItem.toString().toUpperCase().trim());
            } catch (IllegalArgumentException e) {
                javax.swing.JOptionPane.showMessageDialog(this, 
                    "Invalid Role selection format type.", 
                    "Parsing Error", 
                    javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        // 5. Commit the change down to our persistence layer via the UserService instance
        try {
            this.userService.updateUserRole(targetUsername, newRole);

            javax.swing.JOptionPane.showMessageDialog(this, 
                "Successfully updated permissions for " + targetUsername + " to [" + newRole.name() + "].",
                "Role Updated",
                javax.swing.JOptionPane.INFORMATION_MESSAGE);

            // =======================================================================
            // INSTANT REFRESH TRIGGER: Force the table view to reload immediately!
            // =======================================================================
            refreshTableData(); 

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Database update failed: " + e.getMessage());
        }
    }//GEN-LAST:event_btnRoleActionPerformed
    
    

