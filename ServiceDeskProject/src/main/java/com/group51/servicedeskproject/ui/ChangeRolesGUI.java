    private void setupUserTable() {
        // Define column headers for our clean UI layout
        String[] columnNames = {"Username", "Current Access Level"};
        
        // Prevent users from directly typing into the table grid cells
        tableModel = new DefaultTableModel(columnNames, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; 
            }
        };
        
        jTable1.setModel(tableModel);
        
        // Listen for selection changes: when a row is clicked, sync the dropdown choice
        jTable1.getSelectionModel().addListSelectionListener(evt -> {
            if (!evt.getValueIsAdjusting()) {
                syncDropdownWithSelection();
            }
        });
    }
    
    private void populateUserTable() {
        tableModel.setRowCount(0); // Wipe stale data rows
        
        if (userService != null) {
            allUsersCache = userService.getAllUsers();
            for (User user : allUsersCache) {
                Object[] rowData = { user.getUsername(), user.getRole().name() };
                tableModel.addRow(rowData);
            }
        }
    }
    
    private void syncDropdownWithSelection() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1 && allUsersCache != null) {
            User selectedUser = allUsersCache.get(selectedRow);
            roleCombo.setSelectedItem(selectedUser.getRole().name());
        }
    }
    
    private void refreshTableData() {
        // 1. Get the underlying data model from your NetBeans JTable variable name
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();

        // 2. Clear out all old rows so we don't append duplicates
        model.setRowCount(0);

        try {
            // 3. Fetch the fresh, updated list from your persistence layer pipeline
            java.util.List<com.group51.servicedeskproject.model.User> freshUserList = this.userService.getAllUsers();

            // 4. Re-populate the rows with the latest access levels
            if (freshUserList != null) {
                for (com.group51.servicedeskproject.model.User u : freshUserList) {
                    model.addRow(new Object[]{
                        u.getUsername(),
                        u.getRole() != null ? u.getRole().name() : "USER"
                    });
                }
            }
        } catch (Exception e) {
            System.err.println("Failed to pull fresh table data: " + e.getMessage());
        }
    }

